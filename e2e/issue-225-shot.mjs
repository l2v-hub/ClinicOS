import { chromium } from 'playwright';
import { readFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';
const OUT = 'artifacts/task-validation/225-azure-backend-config-principles';
mkdirSync(join(OUT, 'screenshots'), { recursive: true });
mkdirSync(join(OUT, 'trace'), { recursive: true });
const md = readFileSync('docs/azure-backend-config-principles.md', 'utf8');
const esc = md.replace(/&/g, '&amp;').replace(/</g, '&lt;');
const html = `<!doctype html><meta charset="utf-8"><body style="font:14px ui-monospace,monospace;padding:28px;color:#101828;max-width:1000px;margin:auto"><h1 style="font-family:system-ui;color:#0F5FD7">#225 — docs/azure-backend-config-principles.md (PR #251, docs-only)</h1><pre id="doc" style="white-space:pre-wrap;background:#F2F4F7;padding:16px;border-radius:8px;border:1px solid #D0D5DD">${esc}</pre><p id="verdict" style="font-family:system-ui;font-weight:700;color:#0E8A16">Documento presente e coerente · AC1 env/secret model · AC2 provider swappable · AC3 no deploy</p></body>`;
const b = await chromium.launch();
const ctx = await b.newContext({ viewport: { width: 1100, height: 900 } });
await ctx.tracing.start({ screenshots: true, snapshots: true });
const p = await ctx.newPage();
await p.setContent(html, { waitUntil: 'load' });
const has = await p.locator('#doc').innerText();
console.log('AC1 present:', /Env \/ secret model \(AC1\)/.test(has));
console.log('AC2 present:', /swappable \(AC2\)/.test(has));
console.log('AC3 present:', /AC3/.test(has));
await p.screenshot({ path: join(OUT, 'screenshots', 'azure-principles-doc.png'), fullPage: true });
await ctx.tracing.stop({ path: join(OUT, 'trace', 'trace.zip') });
await b.close();
console.log('PASS — screenshot salvato');
