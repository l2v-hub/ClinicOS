  'C:/Users/llavia/AppData/Local/Temp/claude/E--Workspace-DG-SE-DEV-ClinicOS/8ac75c38-1dba-4271-bf9f-bb5bd8973c09/scratchpad/redcheck/intake/parse-discharge-therapy.ts';

const newMod = await import(
  pathToFileURL(`${WT}/backend/src/intake/parse-discharge-therapy.ts`).href
);
const oldMod = await import(pathToFileURL(OLD).href);

// Demonstration input: #156 fixture, then blank lines (the delimiter), then advice prose.
const FIXTURE = [
  'KEPPRA CPR RIV 500 MGR (OS) 1 Cpr ore 08:00 e alle 20:00 dal 03/07/2026 (Classe A)',
  'EUTIROX 100*FL 50CPR 100MCG (OS) 1 Cpr ore 08:00 dal 03/07/2026 (Classe A)',
  'CACIT VIT.D3 BS 1GR/880UI (OS) 1 Dosi ore 08:00 dal 03/07/2026 Mar Gio Sab Dom (Classe A)',
  'KANRENOL CPR 25 MGR (OS) 1 Cpr ore 18:00 dal 03/07/2026 (Classe C)',
  'LASIX CPR 25 MG (OS) 1 Cpr ore 08:00 e alle 14:00 dal 03/07/2026 (Classe A)',
  'FOLINA CPS 5 MGR (OS) 1 Cps ore 08:00 dal 03/07/2026 (Classe A)',
  'LATTULAC*SCIR 200ML 67G/100ML (OS) 1 Dosi ore 08:00 dal 03/07/2026 (Classe )',
  'MEDROL CPR 4 MGR (OS) 1 Cpr ore 08:00 dal 03/07/2026 (Classe C)',
  'CETIRIZINA ZENT*20CPR RIV 10MG (OS) 1/2 Dosi ore 08:00 dal 03/07/2026 (Classe )',
  'PEVARYL POLVERE INGUINE SN X 1 AL DI',
].join('\n');
const PROSE = [
  'Si consiglia controllo cardiologico tra 30 giorni.',
  'Proseguire la fisioterapia respiratoria a domicilio.',
  'In caso di febbre contattare il curante.',
].join('\n');
const DEMO = `${FIXTURE}\n\n${PROSE}`;
const EXPECTED = new Set([
  'KEPPRA',
  'EUTIROX',
  'CACIT',
  'KANRENOL',
  'LASIX',
  'FOLINA',
  'LATTULAC',
  'MEDROL',
  'CETIRIZINA',
  'PEVARYL',
]);

const oldRows = oldMod.parseDischargeTherapy(DEMO);
const newRows = newMod.parseDischargeTherapy(DEMO);
const spurious = (rows) => rows.filter((r) => !EXPECTED.has(r.farmacoNome));
const oldSpurious = spurious(oldRows);
const newSpurious = spurious(newRows);

// Parse the captured TAP of the parser suite (PR head run).
const tap = readFileSync(`${EV}/logs/parser-suite.tap`, 'utf8');
const tests = [...tap.matchAll(/^(ok|not ok) (\d+) - (.+)$/gm)].map((m) => ({
  ok: m[1] === 'ok',
  name: m[3].trim().replace(/\\#/g, '#'),
}));
const totalPass = Number(tap.match(/^# pass (\d+)/m)?.[1] ?? -1);
const totalFail = Number(tap.match(/^# fail (\d+)/m)?.[1] ?? -1);
const t296 = tests.filter((t) => t.name.includes('#296'));

const esc = (s) => s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
const namesList = (rows) => rows.map((r) => r.farmacoNome).join(', ');

